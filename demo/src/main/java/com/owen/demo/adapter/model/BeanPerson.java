package com.owen.demo.adapter.model;


public class BeanPerson {

    private String name;
    private String job;

    public BeanPerson(String name, String job) {
        this.name = name;
        this.job = job;
    }

    public String getName() {
        return name;
    }

    public String getJob() {
        return job;
    }
}
